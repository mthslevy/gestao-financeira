const STORAGE_KEY = 'gestao-financeira-locale'

export function getStoredLocale(): Locale {
  try {
    const stored = localStorage.getItem(STORAGE_KEY) as Locale | null
    if (stored && (stored === 'pt-BR' || stored === 'en' || stored === 'es')) return stored
  } catch {
    /* ignore */
  }
  return 'pt-BR'
}

export function setStoredLocale(locale: Locale): void {
  try {
    localStorage.setItem(STORAGE_KEY, locale)
  } catch {
    /* ignore */
  }
}

export function t(locale: Locale, key: string, params?: Record<string, string>): string {
  const text = translations[locale][key] ?? translations['pt-BR'][key] ?? key
  if (!params) return text
  return Object.entries(params).reduce((acc, [k, v]) => acc.replace(`{${k}}`, v), text)
}
